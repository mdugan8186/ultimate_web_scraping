# ==== http verbs ====

# http verbs are used to describe the type of action that the client wants to perform on the server. there are over 39 verbs, but in practice only a handful are used. the most common ones are GET, POST, PUT, PATH, and DELETE.

# GET is used to retrieve a resource from the server

# POST is used to create a new resource on the server

# PUT is commonly used to update an existing resource on the server

# PATCH is also used to update an existing resource but is meant to be used for partial updates exclusively

# DELETE is used to delete an existing  resource on the server
