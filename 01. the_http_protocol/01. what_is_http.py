# ==== what is HTTP? ====

# HTTP
# - a communication protocol that allows the exchange of information between a client and a server
# this a transfer protocol

# - hypertext transfer protocol
# this is a set of rules that two or more entities use to communicate with each other
# text with hyperlinks

# - three properties of HTTP:
# 1. it is based on requests and responses between a client and a server

# 2. HTTP is meant to be human readable. this is why it is text based and easy to troubleshoot

# 3. naturally extensible by design. new functionality could be added through the use of headers and status codes as long as there's agreement between the client and the server o what those new definitions mean
